"""Reproducible benchmark: recall and scan-fraction of warren's HNSW vs exact.

We build an index over deterministic synthetic data (clustered Gaussians, which
mimic real embedding distributions better than uniform noise), then for a set of
held-out queries compare warren's approximate top-k against the exact top-k from
brute force.

Metrics:
    recall@k    fraction of the true k nearest neighbors that warren returns
    scan        vectors whose distance was actually computed, as a % of the DB
                (this is HNSW's real, language-independent win: sublinear search)
    latency     median wall-clock per query, for reference

Note: this is pure Python + NumPy, so wall-clock does NOT beat a single BLAS
brute-force matmul at these sizes — the honest win is the *scan fraction*, which
shows the index touches only a fraction of the database per query. A C/SIMD
implementation (hnswlib, FAISS) is what turns that into a wall-clock win.

Everything is seeded, so the numbers reproduce exactly.

Usage:
    python benchmarks/bench.py            # print a markdown table
    python benchmarks/bench.py --write    # also update benchmarks/results.md
"""

from __future__ import annotations

import argparse
import time
from pathlib import Path

import numpy as np

from warren import HNSW, BruteForceIndex

RESULTS = Path(__file__).parent / "results.md"

# Defaults for the published run.
N = 4000
DIM = 64
N_QUERIES = 200
N_CLUSTERS = 25
K = 10
EF_VALUES = (10, 50, 100)
EF_CONSTRUCTION = 150
SEED = 7


def make_data(n: int, n_queries: int, dim: int, n_clusters: int, seed: int):
    rng = np.random.default_rng(seed)
    centers = rng.normal(0, 1, size=(n_clusters, dim)).astype(np.float32)
    assign = rng.integers(0, n_clusters, size=n)
    data = centers[assign] + rng.normal(0, 0.35, size=(n, dim)).astype(np.float32)
    q_assign = rng.integers(0, n_clusters, size=n_queries)
    queries = centers[q_assign] + rng.normal(0, 0.35, size=(n_queries, dim)).astype(np.float32)
    return data.astype(np.float32), queries.astype(np.float32)


def recall_at_k(approx: list[tuple], exact: list[tuple], k: int) -> float:
    a = {i for i, _ in approx[:k]}
    e = {i for i, _ in exact[:k]}
    return len(a & e) / len(e) if e else 1.0


def run(
    *,
    n: int = N,
    dim: int = DIM,
    n_queries: int = N_QUERIES,
    n_clusters: int = N_CLUSTERS,
    k: int = K,
    ef_values=EF_VALUES,
    ef_construction: int = EF_CONSTRUCTION,
    seed: int = SEED,
) -> dict:
    data, queries = make_data(n, n_queries, dim, n_clusters, seed)
    ids = list(range(n))

    exact = BruteForceIndex(dim, metric="cosine")
    exact.add(data, ids)

    index = HNSW(dim, metric="cosine", m=16, ef_construction=ef_construction, seed=seed)
    build_start = time.perf_counter()
    index.add(data, ids)
    build_time = time.perf_counter() - build_start

    gt = [exact.search(q, k) for q in queries]

    rows = {}
    for ef in ef_values:
        recalls, scans, latencies = [], [], []
        for q, truth in zip(queries, gt, strict=True):
            index.dist_calls = 0
            t0 = time.perf_counter()
            approx = index.search(q, k, ef=ef)
            latencies.append((time.perf_counter() - t0) * 1000.0)
            recalls.append(recall_at_k(approx, truth, k))
            scans.append(index.dist_calls)
        rows[ef] = {
            "recall": sum(recalls) / len(recalls),
            "scan_pct": 100.0 * (sum(scans) / len(scans)) / n,
            "latency_ms": sorted(latencies)[len(latencies) // 2],
        }
    return {
        "build_time": build_time,
        "rows": rows,
        "config": {"n": n, "dim": dim, "n_queries": n_queries, "n_clusters": n_clusters, "k": k},
    }


def to_markdown(result: dict) -> str:
    c = result["config"]
    lines = [
        f"Database: {c['n']} vectors × {c['dim']} dims ({c['n_clusters']} clusters), "
        f"{c['n_queries']} queries, k={c['k']}, cosine. Build time: {result['build_time']:.2f}s.",
        "",
        f"| ef_search | recall@{c['k']} | DB scanned / query | median latency |",
        "|---|---|---|---|",
    ]
    for ef, r in result["rows"].items():
        lines.append(
            f"| {ef} | {r['recall']:.3f} | {r['scan_pct']:.1f}% | {r['latency_ms']:.2f} ms |"
        )
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--write", action="store_true")
    args = parser.parse_args()

    result = run()
    table = to_markdown(result)
    print(table)

    if args.write:
        RESULTS.write_text(
            "# Benchmark results\n\n"
            "Generated by `python benchmarks/bench.py --write`. Deterministic "
            "(seeded synthetic data), so anyone reproduces these numbers.\n\n"
            "**recall@k** = fraction of the true k nearest neighbors that the "
            "approximate index returns. **DB scanned** = share of the database whose "
            "distance was actually computed (HNSW's sublinear-search win).\n\n"
            f"{table}\n"
        )
        print(f"\nwrote {RESULTS}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
